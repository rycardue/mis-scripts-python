# 1. STRINGS (Cadenas de texto)
# Sirven para guardar texto, nombres o mensajes. 
# Siempre van entre comillas " " o ' '.
nombre = "Programador" 
print(f"Nombre: {nombre} | Tipo: {type(nombre)}")

# 2. INTEGERS (Números enteros)
# Sirven para contar unidades exactas (edad, años, cantidad).
# No llevan comillas ni puntos decimales.
edad = 25 
print(f"Edad: {edad} | Tipo: {type(edad)}")

# 3. FLOATS (Números decimales)
# Sirven para medidas precisas, dinero o resultados científicos.
# En programación usamos el punto (.) para los decimales.
precio = 19.99 
print(f"Precio: {precio} | Tipo: {type(precio)}")

# 4. BOOLEANS (Lógica: Verdadero o Falso)
# Son la base de las decisiones en el código (Sí/No).
# Solo existen dos valores: True o False (la primera letra en Mayúscula).
es_divertido = True
print(f"¿Es divertido?: {es_divertido} | Tipo: {type(es_divertido)}")

# ==========================================
# RESUMEN PARA TU BIBLIOTECA:
# - str: Texto.
# - int: Números sin decimales.
# - float: Números con decimales.
# - bool: Valores lógicos (True/False).
